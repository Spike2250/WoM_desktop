def get_keys(dict_: dict,
             not_dict_keys: dict = {},
             keys: list = [],
             ) -> dict:
    for key in dict_:
        if isinstance(dict_[key], dict):
            keys.append(key)
            not_dict_keys = get_keys(dict_[key], not_dict_keys, keys)
            keys.pop(-1)
        else:
            not_dict_keys[key] = {
                'name': dict_[key],
                'keys': copy.deepcopy(keys),
            }
    return not_dict_keys


def create_domains_lists(domains: dict):
    LIST = {
        's': ['---'],
        'b': ['---'],
        'd': ['---'],
        'e': ['---'],
    }
    multy_domains = ['']

    for key in domains:
        type_ = key[0]
        domain = f"{key} - {domains[key]['name']}"
        domain_reverse = f"{domains[key]['name']} - {key}"

        LIST[type_].append(domain)

        multy_domains.append(domain)
        multy_domains.append(domain_reverse)

    return {
        'all': multy_domains,
        'structure': LIST['s'],
        'function': LIST['b'],
        'activity': LIST['d'],
        'environment': LIST['e'],
    }


