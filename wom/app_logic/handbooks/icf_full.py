    domains = get_keys(ICF)
    keys = list(domains.keys())

    NUMBER_IS_VALID = all((
        (NUMBER > 0),
        (NUMBER <= (KEYS_LENGHT := len(keys)))
    ))
    for i in range(NUMBER if NUMBER_IS_VALID else KEYS_LENGHT):
        DOMAIN = keys[i]
        DOMAIN_NAME = domains[DOMAIN]["name"]
        DOMAIN_PATH_KEYS = domains[DOMAIN]['keys']
        print()
        print('- ' * 45)
        print(f' = = = = = "{DOMAIN}" - {DOMAIN_NAME} = = = = =')
        print('Путь в ICF:')
        k = ' '
        for index, key in enumerate(DOMAIN_PATH_KEYS):
            print(f' {k + k * index}|_ {key}')


